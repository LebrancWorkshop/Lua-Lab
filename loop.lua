-- for loop
print("-- For Loop --")
for f = 1,10,1
do
    print(f)
end
print("#########")

-- while loop
print("-- While Loop --")
w = 1
while(w <= 10)
do
    print(w)
    w = w + 1
end
print("#########")

-- Repeat Until Loop
print("-- Repeat..Until Loop --")
ru = 1
repeat
    print(ru)
    ru = ru + 1
until(ru > 10)
print("#########")


